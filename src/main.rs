use std::io::Cursor;
use std::time::Instant;

fn main() {
    // Create test data
    const COUNT: u64 = 40_000;
    let mut records = Vec::<Record>::with_capacity(COUNT as usize);
    for _ in 0..COUNT {
        // Uses the middle variant to simulate even distribution
        records.push(Record::Record000(256, [0; 256].to_vec()));
    }
    let data_file = DataFile {
        count: COUNT,
        records,
    };
    let mut writer = Cursor::new(Vec::new());
    data_file.write_le(&mut writer).unwrap();
    writer.set_position(0);

    // Read test data
    let now = Instant::now();
    let _ = DataFile::read_le(&mut writer).unwrap();
    let elapsed = now.elapsed();
    println!("Elapsed: {:?}", elapsed);
}

